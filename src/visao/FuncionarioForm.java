    private void habilitarFomulario(boolean status) {
        txf_nome.setEnabled(status);
        txf_cpf.setEnabled(status);
        txf_sexo.setEnabled(status);
        txf_cargo.setEnabled(status);
        txf_salario.setEnabled(status);

    }

    private void atualizarBotao(boolean status) {
        btn_alterar.setEnabled(status);
        btn_Excluir.setEnabled(status);
        btn_Pesquisar.setEnabled(status);
        btn_cadastrar.setEnabled(status);
        btn_Cancelar.setEnabled(!status);
        btn_Salvar.setEnabled(!status);
    }

    private void atualizarTabela() {
        try {
            funcionarios = funcionarioDao.buscarPeloNome(txf_Pesquisa.getText());
            DefaultTableModel model = (DefaultTableModel) tabela_Funcionario.getModel();
            model.setNumRows(0);

            for (int i = 0; i < funcionarios.size(); i++) {
                Funcionario funcionario = funcionarios.get(i);
                model.addRow(new Object[]{funcionario.getNome(), funcionario.getSexo(), funcionario.getCpf(), funcionario.getCargo(), funcionario.getSalario(), });

            }
        } catch (SQLException ex) {
            ex.printStackTrace();

        }

    }


