    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed

        int linha = tabela_Funcionario.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma funcionario", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        funcionarioSelecionada = funcionarios.get(linha);
        try {
            funcionarioDao.remover(funcionarioSelecionada.getId());
            JOptionPane.showMessageDialog(null, "Funcionario excluida");
            atualizarTabela();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_ExcluirActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        int linha = tabela_Funcionario.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma funcionario para alteração", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        atualizarBotao(false);
        habilitarFomulario(true);

        funcionarioSelecionada = funcionarios.get(linha);
        txf_nome.setText(funcionarioSelecionada.getNome());
        txf_sexo.setText(funcionarioSelecionada.getSexo());
        txf_cargo.setText(funcionarioSelecionada.getCargo());
        txf_cpf.setText(funcionarioSelecionada.getSalario());
        txf_cpf.setText(funcionarioSelecionada.getCpf());

    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        atualizarBotao(true);
        habilitarFomulario(false);
        String nome = txf_nome.getText();
        String cpf = txf_cpf.getText();
        String sexo = txf_sexo.getText();
        String cargo= txf_cargo.getText();
        String salario = txf_salario.getText();
        int codigo = 0;

        if (nome.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um nome ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cpf.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um cpf ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (sexo.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um sexo ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }if (cargo.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um cargo ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }if (salario.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um salario ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (funcionarioSelecionada == null) {
            Funcionario funcionario = new Funcionario(nome, cpf, sexo, cargo, salario);

            try {
                funcionarioDao.adicionar(funcionario);
                JOptionPane.showMessageDialog(null, "Funcionario Cadastrada");
                txf_nome.setText("");
                txf_cpf.setText("");
                txf_sexo.setText("");
                txf_cargo.setText("");
                txf_salario.setText("");

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro na hora de inserir os dados", "Erro", JOptionPane.ERROR_MESSAGE);

            }
        } else {
            funcionarioSelecionada.setNome(nome);
            funcionarioSelecionada.setCpf(cpf);
            funcionarioSelecionada.setSexo(sexo);
            funcionarioSelecionada.setCargo(cargo);
            funcionarioSelecionada.setSalario(salario);
            


            try {
                funcionarioDao.alterar(funcionarioSelecionada);
                JOptionPane.showMessageDialog(null, "Funcionario alterado");
                atualizarTabela();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        }
        funcionarioSelecionada = null;
    }//GEN-LAST:event_btn_SalvarActionPerformed

