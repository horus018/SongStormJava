    private void habilitarFomulario(boolean status) {
        txf_nome.setEnabled(status);
        txf_cpf.setEnabled(status);
        txf_sexo.setEnabled(status);
        txf_user_id.setEnabled(status);
        txf_cpf.setEnabled(status);
    }

    private void atualizarBotao(boolean status) {
        btn_alterar.setEnabled(status);
        btn_Excluir.setEnabled(status);
        btn_Pesquisar.setEnabled(status);
        btn_cadastrar.setEnabled(status);
        btn_Cancelar.setEnabled(!status);
        btn_Salvar.setEnabled(!status);
    }

    private void atualizarTabela() {
        try {
            clientes = clienteDao.buscarPeloNome(txf_Pesquisa.getText());
            DefaultTableModel model = (DefaultTableModel) tabela_Cliente.getModel();
            model.setNumRows(0);

            for (int i = 0; i < clientes.size(); i++) {
                Cliente cliente = clientes.get(i);
                model.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getSexo(), cliente.getUser_id()});

            }
        } catch (SQLException ex) {
            ex.printStackTrace();

        }

    }


