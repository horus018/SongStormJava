    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed

        int linha = tabela_Cliente.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }

        clienteSelecionada = clientes.get(linha);
        try {
            clienteDao.remover(clienteSelecionada.getId());
            JOptionPane.showMessageDialog(null, "Cliente excluido");
            atualizarTabela();

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btn_ExcluirActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        int linha = tabela_Cliente.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para alteração", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        atualizarBotao(false);
        habilitarFomulario(true);

        clienteSelecionada = clientes.get(linha);
        txf_nome.setText(clienteSelecionada.getNome());
        txf_sexo.setText(clienteSelecionada.getSexo());
        txf_cpf.setText(clienteSelecionada.getCpf());
        txf_user_id.setText(clienteSelecionada.getUser_id());

    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed
        atualizarBotao(true);
        habilitarFomulario(false);
        String nome = txf_nome.getText();
        String sexo = txf_sexo.getText();
        String cpf = txf_cpf.getText();
        String user_id = txf_user_id.getText();

        int codigo = 0;

        if (nome.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um nome ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (sexo.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um sexo ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }if (cpf.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um cpf ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (user_id.length() == 0) {
            JOptionPane.showMessageDialog(null, "Informe um cpf ", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (clienteSelecionada == null) {
            Cliente cliente = new Cliente(nome, sexo, cpf, user_id);

            try {
                clienteDao.adicionar(cliente);
                JOptionPane.showMessageDialog(null, "Cliente Cadastrado");
                txf_nome.setText("");
                txf_sexo.setText("");
                txf_cpf.setText("");
                txf_user_id.setText("");

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro na hora de inserir os dados", "Erro", JOptionPane.ERROR_MESSAGE);

            }
        } else {
            clienteSelecionada.setNome(nome);
            clienteSelecionada.setSexo(sexo);
            clienteSelecionada.setCpf(cpf);
            clienteSelecionada.setUser_id(user_id);

            try {
                clienteDao.alterar(clienteSelecionada);
                JOptionPane.showMessageDialog(null, "Cliente alterado");
                atualizarTabela();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        }
        clienteSelecionada = null;
    }//GEN-LAST:event_btn_SalvarActionPerformed

